    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        
        try {
             Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException e) {
             e.printStackTrace();
        }                    
    
    if (username.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Please fill username");
    } else if (password.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Please fill password");
    } else if (username.getText().contains("admin") && password.getText().contains("admin123")) {
        JOptionPane.showMessageDialog(null, "Login Successful");
        
         // Close the current login window
        this.dispose();

        // Navigate to the products page
        new Products().setVisible(true);
        
    }
    else if (username.getText().contains("cashier") && password.getText().contains("cashier123")) {
        JOptionPane.showMessageDialog(null, "Login Successful");
        
         // Close the current login window
        this.dispose();

        // Navigate to the products page
        new Cashier().setVisible(true);
        
    }else {
        JOptionPane.showMessageDialog(null, "Wrong username or password");
   }       
    }//GEN-LAST:event_loginBtnActionPerformed

